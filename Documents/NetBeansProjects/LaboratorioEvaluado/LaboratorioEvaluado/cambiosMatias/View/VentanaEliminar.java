    private boolean validarCodigo(String cad)
    {
        try 
        {
            Integer.parseInt(cad);
            return true;
        } 
        catch(NullPointerException e)
        {
            return false;
        }
        catch (NumberFormatException ex) 
        {            
            return false;
        }
    }
    
